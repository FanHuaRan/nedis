package com.fhr.nedis.core.command;

import java.nio.charset.StandardCharsets;

/**
 * Created by Huaran Fan on 2018/11/24
 *
 * @description
 */
public enum RedisCommands {
    //// Key commands
    DEL, DUMP, EXISTS, EXPIRE, EXPIREAT, KEYS, MIGRATE, MOVE, OBJECT, PERSIST, PEXPIRE,
    PEXPIREAT, PTTL, RANDOMKEY, RENAME, RENAMENX, RESTORE, SORT, TTL, TYPE, SCAN,

    /// String commands
    APPEND, BITCOUNT, BITOP, DECR, DECRBY, GET, GETBIT, GETRANGE, GETSET, INCR, INCRBY,
    INCRBYFLOAT, MGET, MSET, MSETNX, PSETEX, SET, SETBIT, SETEX, SETNX, SETRANGE, STRLEN,

    /// Hash commands
    HDEL, HEXISTS, HGET, HGETALL, HINCRBY, HINCRBYFLOAT, HKEYS, HLEN, HMGET, HMSET, HSET,
    HSETNX, HVALS, HSCAN,

    // List commands
    BLPOP, BRPOP, BRPOPLPUSH, LINDEX, LINSERT, LLEN, LPOP, LPUSH, LPUSHX, LRANGE, LREM, LSET,
    LTRIM, RPOP, RPOPLPUSH, RPUSH, RPUSHX,

    /// Set commands
    SADD, SCARD, SDIFF, SDIFFSTORE, SINTER, SINTERSTORE, SISMEMBER, SMEMBERS, SMOVE, SPOP,
    SRANDMEMBER, SREM, SUNION, SUNIONSTORE, SSCAN,

    /// SortedSet commands
    ZADD, ZCARD, ZCOUNT, ZINCRBY, ZRANGE, ZRANGEBYSCORE, ZRANK, ZREM, ZREMRANGEBYRANK, ZREMRANGEBYSCORE,
    ZREVRANGE, ZREVRANGEBYSCORE, ZREVRANK, ZSCORE, ZUNIONSTORE, ZINTERSTORE, ZSCAN,

    /// pub/sub commands
    PSUBSCRIBE, PUBLISH, PUBSUB, PUNSUBSCRIBE, SUBSCRIBE, UNSUBSCRI,

    /// Transaction commands
    DISCARD,
    EXEC, MULTI,
    UNWATCH, WATCH,

    // Script Commands
    EVAL, EVALSHA, SCRIPT_EXISTS, SCRIPT_FLUSH, SCRIPT_KILL, SCRIPT_LOAD,

    // Connection Commands
    AUTH, ECHO, PING, QUIT, SELECT,

    // Server Commands
    BGREWRITEAOF, BGSAVE, CLIENT_GETNAME, CLIENT_KILL, CLIENT_LIST, CLIENT_SETNAME, CONFIG_GET, CONFIG_RESETSTAT,
    CONFIG_REWRITE, CONFIG_SET, DBSIZE, DEBUG_OBJECT, DEBUG_SEGFAULT, FLUSHALL_FLUSHDB, INFO_LASTSAVE,
    MONITOR_PSYNC, SAVE_SHUTDOWN, SLAVEOF_SLOWLOG, SYNC_TIME;

    private static final String WHITE_SPACE_FLAG = "_";

    protected final byte[] bytes;

    RedisCommands() {
        this.bytes = name().replace(WHITE_SPACE_FLAG, " ")
                .getBytes(StandardCharsets.UTF_8);
    }
}
